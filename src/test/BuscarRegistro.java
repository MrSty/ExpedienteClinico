    private void bModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bModificarActionPerformed
        Registro n = new Registro();
        n.setPac(this.tfPaciente.getText());
        n.setDiagnostico(this.tfDiagnostico.getText());
        n.setTratamiento(this.tfTratamiento.getText());
        this.cln.modificarRegistro(n);
    }//GEN-LAST:event_bModificarActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        cln.cargarRegistro();
        Registro n = this.cln.leerRegistroAct();
        this.mostrarRegistro(n);
    }//GEN-LAST:event_formWindowOpened

    private void bEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEliminarActionPerformed
        this.cln.eliminarRegistro();
        this.cln.sigRegistro();
        this.tfDiagnostico.setText("");
        this.tfPaciente.setText("");
        this.tfTratamiento.setText("");
    }//GEN-LAST:event_bEliminarActionPerformed

    private void mostrarRegistro(Registro p) {
        if (p != null) {
            this.tfPaciente.setText(p.getPac());
            this.tfDiagnostico.setText(p.getDiagnostico());
            this.tfTratamiento.setText(p.getTratamiento());
        }
    }
